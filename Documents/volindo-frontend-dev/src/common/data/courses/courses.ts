export const getCourseData = (id: string | string[]) => {
  return items.filter((el: any) => el.id === id);
};

export const getAssociatedCourseIds = (searchId: string | string[]) => {
  const findObject = items.filter((data: any) => data.id === searchId);
  return findObject[0].ids;
};

export const getCoursesFromIds = (compseIds: string | string[]) => {
  const filteredItems = items.filter((data: any) => {
    if (Array.isArray(compseIds)) {
      return compseIds.includes(data.id);
    } else {
      return data.id === compseIds;
    }
  });

  return filteredItems.map((item: any) => item.title);
};

export const getCourseDataById = (searchId: string | string[]) => {
  const findObject = items.filter((data: any) => data.id === searchId);

  let secondObjects;

  if (findObject.length > 0) {
    const compseIds = findObject[0].ids;
    secondObjects = items.filter((data: any) => compseIds.includes(data.id));
  } else {
    return [];
  }

  return [secondObjects[secondObjects.length - 1]];
};
