export const validPhoneWithCountryCode = (
  phoneWithCountryCode: string
): boolean => {
  const phoneNumber = parsePhoneNumberFromString(phoneWithCountryCode);
  if (!phoneNumber) {
    return false;
  }

  return phoneNumber.isValid();
};
