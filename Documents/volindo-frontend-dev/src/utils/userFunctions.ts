export function getFlightsAgentIdFromURL(id: any, isDuffel: boolean = false) {
  if (id && typeof id === 'string') {
    const identificator = isDuffel ? id.replace('D--', '') : id;
    return identificator.split('--')[1];
  } else return '';
}

